    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        x = (int) jLabel_tankAlani.getBounds().getX();
        y = (int) jLabel_tankAlani.getBounds().getY();
     
        
        if(evt.getKeyCode()==KeyEvent.VK_D){
            
          Image icon1_Sag = new ImageIcon(this.getClass().getResource("/tankOyunuFoto_Package/tank1_sag.png")).getImage();
          jLabel_tankAlani.setIcon(new ImageIcon(icon1_Sag));  
          
          if(x<1347) x += 8;
          jLabel_tankAlani.setIcon(new ImageIcon(icon1_Sag));
          jLabel_tankAlani.setBounds(x, y, 128, 128);
        }
        
        if(evt.getKeyCode()==KeyEvent.VK_A){
         
          Image icon1_Sol = new ImageIcon(this.getClass().getResource("/tankOyunuFoto_Package/tank1_sol.png")).getImage();
          jLabel_tankAlani.setIcon(new ImageIcon(icon1_Sol));  
            
          if(x>0) x -= 8;
          jLabel_tankAlani.setIcon(new ImageIcon(icon1_Sol));
          jLabel_tankAlani.setBounds(x, y, 128, 128);
        }
        
        if(evt.getKeyCode()==KeyEvent.VK_W){
         
          Image icon1_Yukari = new ImageIcon(this.getClass().getResource("/tankOyunuFoto_Package/tank1_yukari.png")).getImage();
          jLabel_tankAlani.setIcon(new ImageIcon(icon1_Yukari));   
            
          if(y>0) y -= 8;
          jLabel_tankAlani.setIcon(new ImageIcon(icon1_Yukari));
          jLabel_tankAlani.setBounds(x, y, 128, 128);
        }
        
        if(evt.getKeyCode()==KeyEvent.VK_S){
         
          Image icon1_Asagi = new ImageIcon(this.getClass().getResource("/tankOyunuFoto_Package/tank1_asagi.png")).getImage();
          jLabel_tankAlani.setIcon(new ImageIcon(icon1_Asagi));  
            
          if(y<638) y += 8;
          jLabel_tankAlani.setIcon(new ImageIcon(icon1_Asagi));
          jLabel_tankAlani.setBounds(x, y, 128, 128);
        }
        
        Rectangle tankTipi = jLabel_tankAlani.getBounds();
        Rectangle zombieTipi = jLabel_zombie.getBounds();
        
    

       
    if(tankTipi.intersects(zombieTipi)){
          
           Random random = new Random();
           int maxX = jPanel_oyunAlani.getWidth()-jLabel_zombie.getWidth();
           int maxY = jPanel_oyunAlani.getHeight()-jLabel_zombie.getHeight();
           
           int randomX = random.nextInt(maxX);
           int randomy = random.nextInt(maxY);
           
           jLabel_zombie.setLocation(randomX, randomy);
           jPanel_oyunAlani.repaint();
           kalanZombieSayisi--;
       
              
        jTextField_kalanZombieSayisi.setText(String.valueOf("KALAN ZOMBIE SAYISI : "+kalanZombieSayisi));
        
        if (kalanZombieSayisi==0){
            
            JOptionPane.showMessageDialog(this, "", "GÃ–REV DURUMU", 1, winner_icon);
            this.setVisible(false); 
        }
         
         
      
        
      }

     
        
          
    }//GEN-LAST:event_formKeyPressed

